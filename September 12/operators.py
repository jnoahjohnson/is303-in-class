# Addition
num1 = 5
num2 = 15
mySum = num1 + num2
print(mySum)

# Mixing floats and ints
num1 = 5.0
num2 = 10
mySum = num1 + num2
print(mySum)

# Subtraction
num1 = 5
num2 = 10
myDiff = num2 - num1
print(myDiff)

# Multiplication
num1 = 5
num2 = 10
myProd = num1 * num2
print(myProd)

# Division
num1 = 5
num2 = 10
myQuot = num2 / num1
print(myQuot)

# Eponentiation
num1 = 5
num2 = 10
myExp = num1 ** num2
print(myExp)

# Modulus
num1 = 5
num2 = 10
myMod = num2 % num1
print(myMod)

# Floor Division
# Rounded down to the nearest whole number
num1 = 5
num2 = 10
myFloor = num2 // num1
print(myFloor)

# Rounding
num1 = 5.5
roundedNum = round(num1)
print(roundedNum)

# Rounding multiple digits
num1 = 5.555
roundedNum = round(num1, 1)
print(roundedNum)

print( round(1569, -1) )
print( round(1569, -2) )
print( round(1569, -3) )

